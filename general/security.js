///////////////////////////////////////////////
//////// FUNCTION FOR DECODING BASE 64 ////////
///////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////
// Params for decodeBase64 /////////////////////////////////////////////////////////
//**********************************************************************************
//*data -> String ******************************************************************
//**********************************************************************************
////////////////////////////////////////////////////////////////////////////////////
// *returns a String ///////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////
exports.decodeBase64 = function(data){
	return new Buffer(data, 'base64').toString();
};
///////////////////////////////////////////////
///// END OF FUNCTION FOR DECODING BASE 64 ////
///////////////////////////////////////////////


///////////////////////////////////////////////
//////// FUNCTION FOR ENCODING BASE 64 ////////
///////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////
// Params for base64 ///////////////////////////////////////////////////////////////
//**********************************************************************************
//*data -> String ******************************************************************
//**********************************************************************************
////////////////////////////////////////////////////////////////////////////////////
// *returns a String ///////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////
exports.base64 = function(data){
	return new Buffer(data).toString('base64');
};
///////////////////////////////////////////////
///// END OF FUNCTION FOR ENCODING BASE 64 ////
///////////////////////////////////////////////